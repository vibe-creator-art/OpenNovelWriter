const { htmlToManuscriptMarkdown } = require('./manuscript-edit.cjs')

function buildGeneratedMarkdownHeader(input) {
    const notice = isChineseLanguage(input.language)
        ? '<!-- 由 OpenNovelWriter 生成。请勿直接编辑此文件。 -->'
        : '<!-- Generated from OpenNovelWriter. Do not edit this file directly. -->'
    const metadataLines = Object.entries(input.metadata ?? {})
        .filter(([, value]) => value !== null && value !== undefined && String(value).trim())
        .map(([key, value]) => `<!-- ${key}: ${String(value).trim()} -->`)

    return [
        notice,
        '',
        ...metadataLines,
        `# ${normalizeMarkdownHeading(input.title)}`,
        '',
        `## ${normalizeMarkdownHeading(input.subtitle)}`,
    ].join('\n')
}

function buildNovelWorkspaceOutlineMarkdown(novel) {
    const language = novel.language
    const sortedChapters = [...(novel.chapters ?? [])]
        .map((chapter) => ({
            ...chapter,
            scenes: [...(chapter.scenes ?? [])].sort((left, right) => left.order - right.order),
        }))
        .sort((left, right) => {
            if (left.actNumber !== right.actNumber) return left.actNumber - right.actNumber
            if (left.order !== right.order) return left.order - right.order
            return left.id.localeCompare(right.id)
        })

    const actByNumber = new Map()
    for (const act of novel.acts ?? []) {
        if (Number.isInteger(act.number) && act.number > 0) actByNumber.set(act.number, act)
    }

    const actNumbers = [...new Set([...actByNumber.keys(), ...sortedChapters.map((chapter) => chapter.actNumber)])].sort((a, b) => a - b)
    const chapterNumbersById = new Map()
    sortedChapters.forEach((chapter, index) => chapterNumbersById.set(chapter.id, index + 1))

    const lines = [
        buildGeneratedMarkdownHeader({
            title: novel.title,
            subtitle: isChineseLanguage(language) ? '总结' : 'Outline',
            language,
            metadata: { novel_id: novel.id },
        }),
    ]

    for (const actNumber of actNumbers) {
        const act = actByNumber.get(actNumber) ?? null
        const actChapters = sortedChapters.filter((chapter) => chapter.actNumber === actNumber)

        lines.push('', `<!-- act_number: ${actNumber} -->`, `### ${formatActHeading(actNumber, act?.title, language)}`)
        pushOptionalText(lines, act?.summary)

        for (const chapter of actChapters) {
            const chapterNumber = chapterNumbersById.get(chapter.id) ?? chapter.order
            lines.push(
                '',
                `<!-- chapter_id: ${chapter.id} -->`,
                `<!-- chapter_file: chapters/${chapter.id}.md -->`,
                `#### ${formatChapterHeading(chapterNumber, chapter.title, language)}`
            )

            chapter.scenes.forEach((scene, index) => {
                lines.push('', `<!-- scene_id: ${scene.id} -->`, `##### ${formatSceneHeading(index + 1, language)}`)
                pushOptionalText(lines, scene.summary)
            })
        }
    }

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function buildNovelWorkspaceChapterMarkdown(chapter) {
    const language = chapter.language
    const lines = [
        buildGeneratedMarkdownHeader({
            title: chapter.title,
            subtitle: isChineseLanguage(language) ? '场景' : 'Scenes',
            language,
            metadata: { chapter_id: chapter.id },
        }),
    ]

    const scenes = [...(chapter.scenes ?? [])].sort((left, right) => left.order - right.order)
    for (const [index, scene] of scenes.entries()) {
        lines.push(
            '',
            `<!-- scene_id: ${scene.id} -->`,
            `### ${formatSceneHeading(index + 1, language)}`
        )
        lines.push('', `#### ${isChineseLanguage(language) ? '摘要' : 'Summary'}`)
        pushOptionalText(lines, scene.summary)
        lines.push('', `#### ${isChineseLanguage(language) ? '正文' : 'Content'}`)
        pushOptionalText(lines, htmlToManuscriptMarkdown(scene.content))
    }

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function buildNovelWorkspaceTermMarkdown(input) {
    const language = input.language
    const term = input.term
    const lines = [
        buildGeneratedMarkdownHeader({
            title: term.title,
            subtitle: isChineseLanguage(language) ? '词条' : 'Term',
            language,
            metadata: {
                novel_id: input.novelId,
                term_id: term.id,
                category_id: term.categoryId,
            },
        }),
    ]

    pushNamedOptionalText(lines, isChineseLanguage(language) ? '备注' : 'Subtitle', term.subtitle)
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '分类' : 'Category', term.categoryLabel || term.categoryId)
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '颜色' : 'Color', term.color)
    pushListSection(lines, isChineseLanguage(language) ? '标记' : 'Tags', normalizeStringList(term.tags))
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '别名' : 'Aliases', term.aliases)
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '描述' : 'Description', term.description)
    pushListSection(lines, isChineseLanguage(language) ? '经历' : 'Experiences', splitExperienceLines(term.experiences))
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '资料页笔记' : 'Research Notes', term.researchNotes)
    pushExternalReferences(lines, isChineseLanguage(language) ? '外部链接' : 'External References', term.externalReferences)
    pushRelations(lines, isChineseLanguage(language) ? '关系' : 'Relations', term.relations, language)
    pushNamedOptionalText(
        lines,
        isChineseLanguage(language) ? '追踪状态' : 'Tracking',
        formatTermTrackingPolicy(term.aiContextPolicy, language)
    )

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function buildNovelWorkspaceSnippetIndexMarkdown(input) {
    const language = input.language
    const snippets = [...(input.snippets ?? [])].sort(compareSnippets)
    const lines = [
        buildGeneratedMarkdownHeader({
            title: input.title,
            subtitle: isChineseLanguage(language) ? '片段索引' : 'Snippet Index',
            language,
            metadata: { novel_id: input.id },
        }),
    ]

    if (snippets.length === 0) {
        lines.push('', isChineseLanguage(language) ? '暂无片段。' : 'No snippets yet.')
    } else {
        for (const snippet of snippets) {
            const displayTitle = getSnippetDisplayTitle(snippet, language)
            lines.push(
                '',
                `<!-- snippet_id: ${snippet.id} -->`,
                `<!-- snippet_file: snippets/${snippet.id}.md -->`,
                `### ${normalizeMarkdownHeading(displayTitle)}`,
                '',
                `- ${isChineseLanguage(language) ? '文件' : 'File'}: snippets/${snippet.id}.md`,
                `- ${isChineseLanguage(language) ? '置顶' : 'Pinned'}: ${formatBoolean(snippet.pinned, language)}`
            )
            pushNamedOptionalText(lines, isChineseLanguage(language) ? '预览' : 'Preview', getSnippetPreview(snippet.content))
        }
    }

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function buildNovelWorkspaceSnippetMarkdown(input) {
    const language = input.language
    const snippet = input.snippet
    const lines = [
        buildGeneratedMarkdownHeader({
            title: getSnippetDisplayTitle(snippet, language),
            subtitle: isChineseLanguage(language) ? '片段' : 'Snippet',
            language,
            metadata: {
                novel_id: input.novelId,
                snippet_id: snippet.id,
            },
        }),
    ]

    pushNamedOptionalText(lines, isChineseLanguage(language) ? '标题' : 'Title', snippet.title)
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '置顶' : 'Pinned', formatBoolean(snippet.pinned, language))
    pushNamedOptionalText(lines, isChineseLanguage(language) ? '更新时间' : 'Updated At', snippet.updatedAt)
    lines.push('', `### ${isChineseLanguage(language) ? '内容' : 'Content'}`)
    pushOptionalText(lines, htmlToProjectionText(snippet.content))

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function buildNovelWorkspaceDetailedOutlineMarkdown(input) {
    const language = input.language
    const isAct = input.kind === 'act'
    const heading = isAct
        ? formatActHeading(input.actNumber, input.title, language)
        : formatChapterHeading(input.chapterNumber, input.title, language)
    const subtitle = isChineseLanguage(language)
        ? (isAct ? '卷纲' : '章纲')
        : (isAct ? 'Act Outline' : 'Chapter Outline')
    const metadata = isAct
        ? { novel_id: input.novelId, outline_id: input.outlineId, act_number: input.actNumber }
        : { novel_id: input.novelId, outline_id: input.outlineId, chapter_id: input.chapterId }

    const lines = [
        buildGeneratedMarkdownHeader({ title: heading, subtitle, language, metadata }),
        '',
        `### ${isChineseLanguage(language) ? '细纲' : 'Detailed Outline'}`,
    ]
    pushOptionalText(lines, htmlToProjectionText(input.content))

    return `${lines.join('\n').replace(/\n{3,}/g, '\n\n').trim()}\n`
}

function compareSnippets(left, right) {
    if (Boolean(left?.pinned) !== Boolean(right?.pinned)) return left?.pinned ? -1 : 1
    const updatedDiff = toTimestamp(right?.updatedAt) - toTimestamp(left?.updatedAt)
    if (updatedDiff !== 0) return updatedDiff
    const createdDiff = toTimestamp(right?.createdAt) - toTimestamp(left?.createdAt)
    if (createdDiff !== 0) return createdDiff
    return String(left?.id ?? '').localeCompare(String(right?.id ?? ''))
}

function getSnippetDisplayTitle(snippet, language) {
    const title = typeof snippet?.title === 'string' ? snippet.title.trim() : ''
    if (title) return title

    const text = htmlToProjectionText(snippet?.content ?? '')
    const firstLine = text.split('\n').find((line) => line.trim())?.trim() ?? ''
    if (firstLine) return firstLine

    return isChineseLanguage(language) ? '未命名片段' : 'Untitled snippet'
}

function getSnippetPreview(content) {
    const text = htmlToProjectionText(content)
    if (!text) return ''
    return text.replace(/\s+/g, ' ').trim().slice(0, 160)
}

function formatBoolean(value, language) {
    if (isChineseLanguage(language)) return value ? '是' : '否'
    return value ? 'Yes' : 'No'
}

function toTimestamp(value) {
    const time = new Date(value).getTime()
    return Number.isFinite(time) ? time : 0
}

function formatActHeading(actNumber, title, language) {
    const trimmedTitle = normalizeOptionalMarkdownHeading(title)
    if (isChineseLanguage(language)) {
        return trimmedTitle ? `第 ${actNumber} 卷：${trimmedTitle}` : `第 ${actNumber} 卷`
    }
    return trimmedTitle ? `Act ${actNumber}: ${trimmedTitle}` : `Act ${actNumber}`
}

function formatChapterHeading(chapterNumber, title, language) {
    const trimmedTitle = normalizeOptionalMarkdownHeading(title)
    if (isChineseLanguage(language)) {
        return trimmedTitle ? `第 ${chapterNumber} 章：${trimmedTitle}` : `第 ${chapterNumber} 章`
    }
    return trimmedTitle ? `Chapter ${chapterNumber}: ${trimmedTitle}` : `Chapter ${chapterNumber}`
}

function formatSceneHeading(sceneNumber, language) {
    return isChineseLanguage(language) ? `场 ${sceneNumber}` : `Scene ${sceneNumber}`
}

function pushNamedOptionalText(lines, heading, value) {
    const normalized = typeof value === 'string' ? value.trim() : ''
    if (!normalized) return
    lines.push('', `### ${heading}`, '', normalized)
}

function pushListSection(lines, heading, values) {
    if (!values.length) return
    lines.push('', `### ${heading}`, '')
    for (const value of values) {
        lines.push(`- ${value}`)
    }
}

function pushExternalReferences(lines, heading, references) {
    if (!Array.isArray(references) || references.length === 0) return
    const items = references
        .map((reference) => {
            if (!reference || typeof reference !== 'object') return ''
            const url = typeof reference.url === 'string' ? reference.url.trim() : ''
            return url
        })
        .filter(Boolean)
    pushListSection(lines, heading, items)
}

function pushRelations(lines, heading, relations, language) {
    if (!Array.isArray(relations) || relations.length === 0) return
    const items = []
    for (const relation of relations) {
        if (!relation || typeof relation !== 'object') continue
        const otherTitle = typeof relation.otherTitle === 'string' && relation.otherTitle.trim()
            ? relation.otherTitle.trim()
            : relation.otherId
        if (!otherTitle) continue
        const direction = formatRelationDirection(relation.direction, language)
        const label = typeof relation.label === 'string' && relation.label.trim() ? ` (${relation.label.trim()})` : ''
        items.push(`${direction} ${otherTitle}${label}`)
    }
    pushListSection(lines, heading, items)
}

function pushOptionalText(lines, value) {
    const normalized = typeof value === 'string' ? value.trim() : ''
    if (normalized) lines.push(normalized)
}

function splitExperienceLines(value) {
    if (typeof value !== 'string') return []
    return value
        .split('\n')
        .map((line) => line.trim())
        .filter(Boolean)
}

function normalizeStringList(values) {
    if (!Array.isArray(values)) return []
    return values.map((value) => (typeof value === 'string' ? value.trim() : '')).filter(Boolean)
}

function formatRelationDirection(direction, language) {
    if (direction === 'incoming') return isChineseLanguage(language) ? '来自' : 'from'
    if (direction === 'bidirectional') return isChineseLanguage(language) ? '双向' : 'with'
    return isChineseLanguage(language) ? '指向' : 'to'
}

function formatTermTrackingPolicy(policy, language) {
    const normalized = policy === 'always' || policy === 'never' ? policy : 'detected'
    if (isChineseLanguage(language)) {
        if (normalized === 'always') return '始终包含'
        if (normalized === 'never') return '永不包含'
        return '检测到时包含'
    }
    if (normalized === 'always') return 'Always include'
    if (normalized === 'never') return 'Never include'
    return 'Include when detected'
}

function normalizeMarkdownHeading(value) {
    return typeof value === 'string' && value.trim() ? value.trim().replace(/\s+/g, ' ') : 'Untitled'
}

function normalizeOptionalMarkdownHeading(value) {
    return typeof value === 'string' && value.trim() ? value.trim().replace(/\s+/g, ' ') : ''
}

function isChineseLanguage(language) {
    return typeof language === 'string' && language.trim().toLowerCase().startsWith('zh')
}

function htmlToProjectionText(html) {
    if (typeof html !== 'string' || !html.trim()) return ''

    return html
        .replace(/<br\s*\/?>/gi, '\n')
        .replace(/<\/(p|h[1-6]|blockquote|pre)\s*>/gi, '\n\n')
        .replace(/<\/(li|tr)\s*>/gi, '\n')
        .replace(/<[^>]+>/g, ' ')
        .replace(/&nbsp;/g, ' ')
        .replace(/&amp;/g, '&')
        .replace(/&lt;/g, '<')
        .replace(/&gt;/g, '>')
        .replace(/&quot;/g, '"')
        .replace(/&#39;/g, "'")
        .replace(/\u00A0/g, ' ')
        .replace(/\r\n?/g, '\n')
        .replace(/[ \t]+\n/g, '\n')
        .replace(/\t+\n/g, '\n')
        .replace(/\n{3,}/g, '\n\n')
        .trim()
}

